"""SumOfNumber"""

def main():
    """beom"""
    num = int(input())
    summm = 0
    while 0 < 1:
        eee = int(input())
        if eee < 0:
            pass
        elif num == eee:
            return num
        else:
            summm = summm + eee
        if eee == -1 or summm == num:
            return summm
print(main())
